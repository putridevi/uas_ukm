    <h3> UKM UNESA</h3>
    <p>Tari cendrawasih mengawali serangkaian acara ekspo UKM Unesa 2012, tarian asal Bali ini dipentaskan oleh Ni Made Wahyuni dari Jurusan Matematika dan Delia dari Jurusan Tata Busana Unesa dari UKM UKKH. Tarian yang mereka pentaskan mendapatkan apresiasi dari mahasiswa baru dan tamu undangan. Ekspo UKM Unesa pada tahun ini diikuti oleh 58 UKM yang ada di Unesa dan di buka oleh Prof. Dr. Muchlas Samani, selaku rektor.

Dalam sambutannya, Muchlas Samani berpesan kepada mahasiswa baru agar ikut serta dan aktif dalam UKM yang ada di Unesa. Setelah sambutan, rektor memukul gong sebagai tanda dibukanya ekspo UKM Unesa 2012 secara resmi dan disambut dengan tepuk tangan meriah oleh mahasiswa baru.

Terlihat antusiame mahasiswa baru dalam mengunjungi ekspo UKM. Bahkan, mereka langsung mendaftar sebagai anggota UKM di lokasi pameran. Ekspo UKM ini berlangsung selama 2 hari 29--30 Agustus 2012. (Ari/Putri/syt)</p>
